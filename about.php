                <h1>教育部體育署補助各級學校約用運動防護員巡迴服務計畫</h1>
                <p></p>
                <ul class="ulsign">
                    <li class="ulsign">一、教育部體育署（以下簡稱本署）為執行高級中等以下學校體育班設立辦法第十一條規定，補助學校約用運動防護員從事巡迴服務，特訂定本計畫。</li>
                    <li class="ulsign">二、申請資格
                        <ul>
                            <li class="lisign">(一) 設有體育班之高級中等以下學校。</li>
                            <li class="lisign">(二) 非設有體育班，參與近三學年度本署辦理學生運動聯賽成績優異之高級中等以下學校，成績標準如下：
                                <ol>
                                    <li>棒球（木棒組）：前六名。</li>
                                    <li>女子壘球：前三名。</li>
                                    <li>籃球：男、女甲級前六名。</li>
                                    <li>排球：男、女甲級前三名。</li>
                                    <li>足球：男、女甲級前三名。</li>
                                </ol>
                            </li>
                        </ul>
                    </li>
                    <li class="ulsign">三、本計畫補助經費之核給原則如下：
                        <ul>
                            <li class="lisign">(一) 以補助約用持有本署（或行政院體育委員會）核發之運動防護員證照或物理治療師國家證照之運動防護員所需人事費為原則。但本署得考量實際需要，酌予補助執行運動防護工作相關之國內出差旅費、耗材、設備、保險及雜支。</li>
                            <li class="lisign">(二) 人事費薪資標準由本署另訂之，其餘經費項目依據教育部補助及委辦經費核撥結報作業要點規定編列。</li>
                            <li class="lisign">(三) 地方政府主管各級學校，由本署依據中央對直轄市及縣（市）政府補助辦法，依地方政府財力級次給予不同補助比率，並以高級中等學校為優先補助對象。 地方政府及各級學校應編列配合款支應本計畫相關預算。
                            </li>
                        </ul>
                    </li>
                    <li class="ulsign">四、本計畫之申請及審查程序，規定如下：
                        <ul>
                            <li class="lisign">(一) 申請時間：依本署指定之受理時間，提出申請。</li>
                            <li class="lisign">(二) 申請程序：
                                <ol>
                                    <li>於「校園運動防護資訊網」進行線上登錄，填寫及上傳申請資料後，列印申請計畫書（以下簡稱計畫書），檢附佐證資料，計畫書格式如附件。</li>
                                    <li>教育部主管之學校提出計畫書應經學校首長權責核章，逕送本署審查。</li>
                                    <li>各地方政府所屬學校提出計畫書應經學校首長權責核章，送各該地方政府自行初審後，經地方政府相關權責單位核章，彙送本署審查。</li>
                                </ol>
                            </li>
                            <li class="lisign">(三) 審查作業及程序：
                                <ol>
                                    <li>由本署成立審查小組，依評分項目進行審查，並視本署年度預算，擇優辦理。</li>
                                    <li>申請文件不全或其內容不完備者，得通知限期補正；限期內未補正者，逕予審查。</li>
                                    <li>審查小組依申請單位整體條件評估審核。</li>
                                    <li>審查結果，由本署另行通知。</li>
                                </ol>
                            </li>
                            <li class="lisign">(四) 評分項目及配分比率如下：
                                <ol class="thridsign">
                                    <li>運動防護員巡迴服務之主聘學校及區域夥伴學校規劃：百分之五十。</li>
                                    <li>協助地方政府共同推動區域運動人才銜接與培育情形：百分之二十。</li>
                                    <li>輔導及支援網絡建置情形：百分之二十。</li>
                                    <li>近三年運動成績：百分之十。</li>
                                </ol>
                            </li>
                        </ul>
                    </li>
                    <li class="ulsign">五、本署得視政策需求分別匡列教育部主管學校及地方政府所屬學校核定補助名額，並分組審查評分。
                        <div style="text-indent : 2em;">本署以前年度核定有案之計畫，三年內免經前點申請及審查程序。</div>
                        <div style="text-indent : 2em;">於體育類高級中等學校、完全中學設體育班之運動防護員以服務 本校學生、選手為主，得免巡迴他校。</div>
                    </li>
                    <li class="ulsign">六、本計畫執行策略，如下：
                        <ul>
                            <li class="lisign">(一) 運動傷害防護與管理。</li>
                            <li class="lisign">(二) 區域醫療網建置。</li>
                            <li class="lisign">(三) 運動防護教育。</li>
                        </ul>
                    </li>
                    <li class="ulsign">七、運動防護員依第六點執行策略，工作如下：
                        <ul>
                            <li class="lisign">(一) 建置運動防護室運作流程，推動運動防護工作，定時更新選手運動傷害處理紀錄與工作報告。</li>
                            <li class="lisign">(二) 協助區域輔導中心及學校辦理基礎運動防護教育與座談會，並提升鄰近醫療院所共同照護選手健康之合作意願，俾建構區域醫療服務資源網。</li>
                            <li class="lisign">(三) 協助運動選手健康評估與管理及遭遇重大運動傷害之急救處理與轉診就醫流程。</li>
                            <li class="lisign">(四) 擬定運動傷害預防及機能重建計畫，進行後續諮商與教育，並指導大專校院運動防護實習生執行運動傷害防護工作，掌握機能重建進度與檢視恢復成效。</li>
                        </ul>
                    </li>
                    <li class="ulsign">八、本署為建置區域學校運動防護體系，得依行政協助或政府採購法程序，委請（託）開設運動防護相關專業課程之大專校院擔任區域輔導中心，對運動防護員之工作進行專業評估輔導，並協助整合區域醫療資源，建立後送醫療體系、辦理運動防護教育、增能訓練等相關運動傷害預防及健康管理事宜。</li>
                    <li class="ulsign">九、依本計畫補助之計畫，考核及督導之方式如下：
                        <ul>
                            <li class="lisign">(一) 地方政府所屬學校或教育部主管之學校：組成運動防護服務推動小組，定期召開會議，進行自我成效考核，考核結果自行上傳「校園運動防護資訊網」。</li>
                            <li class="lisign">(二) 本署：得辦理督導及考核，作為下年度補助之參考。</li>
                            <li class="lisign">(三) 受補助學校約用之運動防護員經學年度考核通過者，始得續任用之，考核評分表送本署指定單位備查。</li>
                        </ul>
                    </li>
                    <li class="ulsign">十、依本計畫補助之各項經費，應專款專用，並依核定計畫內容執行。其請撥、支用及核銷，應依教育部補助及委辦經費核撥結報作業要點規定辦理。
                    </li>
                    <li class="ulsign">十一、受補助單位檢送成果報告應包括質（量）化執行情形、照片、 檢討（改進、建議）及經費執行明細。
                    </li>
                </ul>
